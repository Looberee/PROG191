    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        String tblCourse = tblModel.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String tblName = tblModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tblID = tblModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblDate = tblModel.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String tblStatus = tblModel.getValueAt(jTable1.getSelectedRow(), 4).toString();
        //------------------------------------------------------------------//
        cbCourse.setSelectedItem(tblCourse);
        txtName.setText(tblName);
        txtID.setText(tblID);
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tblDate);

            chDate.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Application.class.getName()).log(Level.SEVERE, null, ex);
        }
        if ("Basic".equals(tblStatus)) {
            rdBasic.doClick();
        } else if ("Intermidiate".equals(tblStatus)) {
            rdInter.doClick();
        } else {
            rdAdvanced.doClick();
        }
    }//GEN-LAST:event_jTable1MouseClicked

