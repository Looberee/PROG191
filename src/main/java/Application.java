    public void saveData() {
        try {
            FileWriter fileWriter = new FileWriter("C:\\Users\\USER\\Documents\\NetBeansProjects\\TestNetBean\\save.txt");
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                for (int j = 0; j < jTable1.getColumnCount(); j++) {
                    fileWriter.write((String) jTable1.getModel().getValueAt(i, j) + "  ");
                }
                fileWriter.write("\n");
            }
            fileWriter.close();
            JOptionPane.showMessageDialog(this, "Save success");
            btnLoad.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }

