    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            String eName = txtName.getText();
            String eCourse = cbCourse.getSelectedItem().toString();
            String eNum = txtID.getText();
            SimpleDateFormat Fdate = new SimpleDateFormat("yyyy-MM-dd");
            String eDate = Fdate.format(chDate.getDate());
            rdInter.setActionCommand("Intermediate");
            rdBasic.setActionCommand("Basic");
            rdAdvanced.setActionCommand("Advanced");
            if (validID(txtID.getText())) {
                JOptionPane.showMessageDialog(this, "Invalid ID");
            } else if (validName(txtName.getText().replaceAll(" ", ""))) {
                JOptionPane.showMessageDialog(this, "Invalid name");
            } else {
                String eLevel = buttonGroup1.getSelection().getActionCommand();
                tblModel.setValueAt(eCourse, jTable1.getSelectedRow(), 2);
                tblModel.setValueAt(eName, jTable1.getSelectedRow(), 1);
                tblModel.setValueAt(eNum, jTable1.getSelectedRow(), 0);
                tblModel.setValueAt(eDate, jTable1.getSelectedRow(), 3);
                tblModel.setValueAt(eLevel, jTable1.getSelectedRow(), 4);
                JOptionPane.showMessageDialog(this, "Edit succesfully");
            }
        } else {
            if (jTable1.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Empty Table");
            } else {
                JOptionPane.showMessageDialog(this, "Select a row to procceed");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

